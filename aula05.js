/* Operadores Relacionais
>  --> maior que
>= --> maior ou igual que
<  --> menor que
<= --> menor ou igual que
== --> igual (= sozinho é apenas atribuição)
!= --> diferente
*/

let num1 = 10;
let num2 = 5;
let num3 = 10;

console.log(num1 > num2);
console.log(num1 >= num3);
console.log(num2 > num1);
console.log(num2 >= num1);
console.log(num3 == num1);
console.log(num3 != num1);
console.log(!(num3 != num1));
